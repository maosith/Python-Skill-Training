a = input("Digite algo: ")
print("O tipo primitivo é da classe: ", type(a))
print("Só tem espaços?", a.isspace())
print("É alfanumerico?", a.isalnum())
print("É afalbetico?", a.isalpha())
print("Tem letras maiusculas?", a.isupper())
print("É um número?", a.isnumeric())
print("Está em mínusculas?", a.islower())
print("Está capitalizada?", a.istitle())







